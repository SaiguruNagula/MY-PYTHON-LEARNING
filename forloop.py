# for loop : used to exectute code for fix amount of time or for a fix amount of data

# for loop is used to iterate over a sequence (like a list, tuple, dictionary, set, or string) or other iterable objects.

# It is less memory efficient than while loop but more readable and easier to use.

for x in range(1,11): # range(start, stop, step)  # start is inclusive and stop is exclusive
    print(x) # 1 to 10

for x in reversed(range(1,11)): # range(start, stop, step)  # start is inclusive and stop is exclusive
    print(x) # 10 to 1